package dsq.sedition.collision;

public class Line2D {
    
    public final float x1;
    public final float z1;
    public final float x2;
    public final float z2;

    public Line2D(final float x1, final float z1, float x2, float z2) {
        this.x1 = x1;
        this.z1 = z1;
        this.x2 = x2;
        this.z2 = z2;
    }
}
