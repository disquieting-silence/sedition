package dsq.sedition.view;

public class Box {
    public final int x;
    public final int y;
    public final int width;
    public final int height;

    public Box(final int x, final int y, final int width, final int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
